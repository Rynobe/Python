mystr = 'Hellotebuta'
print(mystr[2:])    # a harmadik betütől mutatja.
print(mystr[:3])    # az első három betüt mutatja.
print(mystr[0:5])   # az elsőtől az ötödik betűt mutatja.
print(mystr[::2])   # minden harmadik betűt mutatja az elsővel kezdve.
print(mystr[2:7:2]) # a harmadik betűtől a nyolcadik betűig és minden 3 betűt mutatja.

